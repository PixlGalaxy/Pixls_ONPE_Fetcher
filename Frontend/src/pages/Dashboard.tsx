  const top5 = allCandidates.slice(0, 5);
  const displayed = showAll ? allCandidates : top5;

  const trends = useMemo(() => {
    const map: Record<string, CandidateTrend> = {};
    if (!current || history.length === 0) return map;
    const sorted = [...history].sort((a, b) =>
      new Date(b.snapshot_time).getTime() - new Date(a.snapshot_time).getTime()
    );
    const prev = sorted.find(s => s.snapshot_time !== current.snapshot_time) ?? sorted[0];
    if (!prev) return map;
    for (const c of current.candidates) {
      const prevC = prev.candidates.find(p => p.candidate_id === c.candidate_id);
      if (prevC) {
        map[c.candidate_id] = {
          pctChange: c.percentage - prevC.percentage,
          votesChange: (c.votes ?? 0) - (prevC.votes ?? 0),
        };
      }
    }
    return map;
  }, [current, history]);

  const lastUpdate = status?.elections?.presidential?.last_snapshot_time;
  const formatted = lastUpdate
    ? new Date(lastUpdate).toLocaleString('es-PE', {
        day: '2-digit', month: '2-digit', year: 'numeric',
        hour: '2-digit', minute: '2-digit', second: '2-digit',
      })
    : null;

  const sched = status?.scheduler;
  const lastCheck = sched?.last_check_time
    ? new Date(sched.last_check_time).toLocaleString('es-PE', { hour: '2-digit', minute: '2-digit', second: '2-digit' })
    : null;
  const lastChange = sched?.last_change_detected
    ? new Date(sched.last_change_detected).toLocaleString('es-PE', { hour: '2-digit', minute: '2-digit', second: '2-digit' })
    : null;
  const isCached = !!(formatted && lastCheck && formatted !== lastCheck);

  const [countdown, setCountdown] = useState<string | null>(null);
